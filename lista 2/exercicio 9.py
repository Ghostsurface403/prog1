# 9. Escreva um programa que leia um número e escreva se ele é igual a 5, a 200, a 400, se
# está no intervalo  entre 500 e 1000, inclusive, ou se ele está fora dos escopos anteriores.

num = int(input('Digite um número: '))
if num == 5:
    print('O número é igual a 5.')
elif num == 200:
    print('O número é igual a 200.')
elif num == 400:
    print('O número é igual a 400.')

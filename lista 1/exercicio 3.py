# 3. Faça um programa que leia 3 notas e seus respectivos pesos, calcule e
# mostre a média ponderada das notas

nota1 = float(input('insira a 1° nota '))
nota2 = float(input('insira a 2° nota '))
nota3 = float(input('insira a 3° nota '))

media_ponderada = ((nota1*1) + (nota2*2) + (nota3*3)) / 3

print(media_ponderada)
